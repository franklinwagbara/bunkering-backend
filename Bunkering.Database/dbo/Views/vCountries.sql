CREATE VIEW dbo.vCountries
AS
SELECT dbo.Countries.Id AS CountryId, dbo.Countries.Name AS CountryName, dbo.States.Id AS StateId, dbo.States.Name AS StateName, dbo.LGAs.Id AS LGAId, dbo.LGAs.Name AS LGA_Name, dbo.Companies.Id AS CompanyId, 
                  dbo.Companies.Name AS CompanyName
FROM     dbo.States INNER JOIN
                  dbo.Companies ON dbo.States.Id = dbo.Companies.StateId INNER JOIN
                  dbo.Countries ON dbo.Companies.CountryId = dbo.Countries.Id INNER JOIN
                  dbo.LGAs ON dbo.States.Id = dbo.LGAs.StateId
GO
